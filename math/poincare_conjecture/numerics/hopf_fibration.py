import numpy as np


# ===========================================================
# The Hopf map
# ===========================================================
# (z₁, z₂) ∈ S³ ⊂ C² ⟶ S² ⊂ R³
# Using real coordinates: write z₁ = a + bi, z₂ = c + di, with a²+b²+c²+d² = 1
# Then:
#   x = 2(ac + bd)   (= 2 Re(z₁ z̄₂))
#   y = 2(bc - ad)   (= 2 Im(z₁ z̄₂))
#   z = a² + b² - c² - d²
#
# Verify x² + y² + z² = (a² + b² + c² + d²)² = 1.

def hopf_map(p):
    """
    Hopf map S³ → S² in real coordinates.
    p = (a, b, c, d) ∈ R⁴ with |p| = 1
    Returns (x, y, z) ∈ R³ on S².
    """
    a, b, c, d = p
    x = 2 * (a*c + b*d)
    y = 2 * (b*c - a*d)
    z = a**2 + b**2 - c**2 - d**2
    return np.array([x, y, z])


