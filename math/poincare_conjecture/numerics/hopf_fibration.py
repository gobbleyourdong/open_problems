# ===========================================================
# Hopf fibers as great circles
# ===========================================================
# The fiber over (x, y, z) ∈ S² is the set of (a, b, c, d) ∈ S³ with H(a,b,c,d) = (x,y,z).
# Parametrize: if (z₁, z₂) is one preimage, then (e^(iθ) z₁, e^(iθ) z₂) for θ ∈ [0, 2π) is the whole fiber.
#
# This shows the fiber is a CIRCLE in S³, parameterized by θ.
# It's a GREAT CIRCLE because the U(1) action is by isometries.

def hopf_fiber(point_on_s2, n_samples=100):
    """
    Compute the Hopf fiber over a point on S².
    Returns n_samples points in S³.
    """
    x, y, z = point_on_s2
    # Find one preimage analytically
    # The fiber over (0, 0, 1) is {(z, 0) : |z|=1} = circle in z₁ direction
    # General case: use a stereographic-like construction

    # Special case: if (x, y, z) = (0, 0, 1), then preimage has c² + d² = 0, so c = d = 0
    # Then a² + b² = 1, fiber is {(cos θ, sin θ, 0, 0) : θ}
    if z > 0.99:
        thetas = np.linspace(0, 2 * np.pi, n_samples, endpoint=False)
        return np.array([[np.cos(t), np.sin(t), 0, 0] for t in thetas])
    elif z < -0.99:
        thetas = np.linspace(0, 2 * np.pi, n_samples, endpoint=False)
        return np.array([[0, 0, np.cos(t), np.sin(t)] for t in thetas])
    else:
        # Use parameterization for general (x, y, z) on S²
        # (a + bi, c + di) with H(a,b,c,d) = (x,y,z)
        # One choice: solve a² + b² = (1+z)/2, c² + d² = (1-z)/2
        # Phase relation: ac + bd = x/2, bc - ad = y/2
        s1 = np.sqrt((1 + z) / 2)  # |z₁|
        s2 = np.sqrt((1 - z) / 2)  # |z₂|

        # Choose initial phases: z₁ = s1, z₂ = (x + iy)/(2 s1)
        # (when s1 > 0; otherwise use the south-pole branch)
        if s1 > 1e-9:
            a0, b0 = s1, 0.0
            c0, d0 = x / (2 * s1), y / (2 * s1)
            # Verify c0² + d0² = s2²
            mag_sq = c0**2 + d0**2
            if abs(mag_sq - s2**2) > 1e-9:
                # Renormalize
                fac = s2 / np.sqrt(mag_sq)
                c0, d0 = c0 * fac, d0 * fac
        else:
            a0, b0 = 0.0, 0.0
            c0, d0 = s2, 0.0

        # Apply U(1) rotation by phase θ: (z₁, z₂) → (e^(iθ) z₁, e^(iθ) z₂)
        thetas = np.linspace(0, 2 * np.pi, n_samples, endpoint=False)
        fiber = np.zeros((n_samples, 4))
        for i, t in enumerate(thetas):
            ct, st = np.cos(t), np.sin(t)
            fiber[i] = [
                ct * a0 - st * b0,
                st * a0 + ct * b0,
                ct * c0 - st * d0,
                st * c0 + ct * d0,
            ]
        return fiber


def test_fibers_are_circles():
    """Verify Hopf fibers are great circles in S³."""
    print("=" * 70)
